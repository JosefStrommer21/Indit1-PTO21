def summe(summand1, summand2):     #Funktion -> Funktionsnamen(Parameter1, Parameter2,...)
    zwischensumme = sumand1 + summand2
    return zwischensumme

Wert1 = summe(23, 24)
print("Summe: ", Wert1)

Wert2 = summe(1, -3)
print("Summe: ", Wert2)

Wert3 = summe(Wert1, Wert2)
print("Summe: ", Wert3)
    
    